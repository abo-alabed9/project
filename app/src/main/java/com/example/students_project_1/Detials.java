package com.example.students_project_1;

public class Detials {

    private String avg;
    private String name;

    public Detials(String avg, String name) {
        this.avg = avg;
        this.name = name;
    }

    public String getAvg() {
        return avg;
    }

    public void setAvg(String avg) {
        this.avg = avg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
